package abc;

public class HangHoa {
	String loai;
	String mahang;
	String tenhanghoa;
	double gianhap;
	int soluongtonkho;
	String ngaynhapkho;

	public HangHoa(String loai, String mahang, String tenhanghoa, double gianhap, int soluongtonkho, String ngaynhapkho) {
		super();
		this.loai = loai;
		this.mahang = mahang;
		this.tenhanghoa = tenhanghoa;
		this.gianhap = gianhap;
		this.soluongtonkho = soluongtonkho;
		this.ngaynhapkho = ngaynhapkho;
	}	
	
}
